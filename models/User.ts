import mongoose, { model, models, Schema } from 'mongoose';
import bcrypt from 'bcryptjs';

export interface UserInterface {
  email: string;
  password: string;
  _id?: mongoose.Types.ObjectId;
  createdAt?: Date;
  updatedAt?: Date;
}

const userSchema = new Schema<UserInterface>(
  {
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    createdAt: {
      type: Date,
      default: Date.now(),
    },
    updatedAt: {
      type: Date,
      default: Date.now(),
    },
  },
  {
    timestamps: true,
  }
);

// Before saving, we need to perform hashing if the password is modified
userSchema.pre('save', async function (next) {
  if (this.isModified('password')) {
    this.password = await bcrypt.hash(this.password, 10);
  }
  // next() for continuing the save operation after hashing password
  next();
});

const User = models?.User || model<UserInterface>('User', userSchema);

export default User;
